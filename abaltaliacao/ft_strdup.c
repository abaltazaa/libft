#include "libft.h"

char	*ft_strdup(const char *src)
{
	char			*dest;
	unsigned int	a;

	dest = malloc(ft_strlen(src) * sizeof (char) + 1);
	if (!dest)
		return (0);
	a = 0;
	while (src[a])
	{
		dest[a] = src[a];
		a++;
	}
	dest[a] = '\0';
	return (dest);
}
